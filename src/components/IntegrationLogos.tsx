type Provider = 'notion' | 'atlassian' | 'confluence' | 'github' | 'gong' | 'slack';

interface IntegrationLogosProps {
  size?: number;
  provider: Provider;
  className?: string;
}

const iconMap: Partial<Record<Provider, keyof typeof simpleIcons>> = {
  notion: 'siNotion',
  atlassian: 'siAtlassian',
  confluence: 'siConfluence',
  github: 'siGithub',
};

export function IntegrationLogos({ size = 24, provider, className }: IntegrationLogosProps) {
  if (provider === 'slack') {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        role="img"
        aria-label="Slack"
        className={cn('text-[var(--text-primary)]', className)}
      >
        <title>Slack</title>
        <path d={SLACK_PATH} fill="currentColor" />
      </svg>
    );
  }

  if (provider === 'gong') {
    return (
      <svg
        width={size}
        height={size}
        viewBox="-0.2 0 38.5 42"
        role="img"
        aria-label="Gong"
        className={cn('text-[var(--text-primary)]', className)}
      >
        <title>Gong</title>
        <path d={GONG_PATH} fill="currentColor" />
      </svg>
    );
  }

  const iconKey = iconMap[provider];
  if (!iconKey) return null;

  const icon = simpleIcons[iconKey] as { title: string; hex: string; path: string } | undefined;
  if (!icon) return null;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      role="img"
      aria-label={icon.title}
      className={cn('text-[var(--text-primary)]', className)}
    >
      <title>{icon.title}</title>
      <path d={icon.path} fill="currentColor" />
    </svg>
  );
}
